    private void cbFechaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbFechaItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (cbFecha.getSelectedIndex() == 0) {
                lista = multaD.listar();
                cargarTabla();
            } else {
                try {
                    lista = Utilidades.listaFechas(multaD.listar(), cbFecha.getSelectedItem().toString());
                    cargarTabla();
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_cbFechaItemStateChanged

