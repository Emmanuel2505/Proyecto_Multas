    public void cargarTabla(){
        modelo.setListaMultas(lista);
        modelo.setPersonas(personaD);
        modelo.setVehiculos(vehiculoD);
        modelo.setListaLicencia(licenciaD);
        tbMultas.setModel(modelo);
        tbMultas.updateUI();
    }

