    private void tbLicenciaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbLicenciaMouseClicked
        // TODO add your handling code here:
        int fila = tbLicencia.getSelectedRow();
        if (fila >= 0) {
            Licencia licencia = (Licencia) ls.obtenerPorPosicion(fila);
            Persona persona = (Persona) personaD.obtenerPersona(licencia.getIdPersona());
            lbNroLicencia.setText(licencia.getNroLicencia());
            lbFecha.setText(licencia.getFechaCaducidad());
            tipos = licencia.getTipos();
            cargarTablaPersona();
        }
    }//GEN-LAST:event_tbLicenciaMouseClicked

    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        int fila = tbLicencia.getSelectedRow();
        if (fila >= 0) {
            if (txfPuntos.getText().length() > 0) {
                Licencia licencia = (Licencia) ls.obtenerPorPosicion(fila);
                licencia.setPuntos(Float.parseFloat(txfPuntos.getText()));
                if (licenciaD.editar(licencia.getIdPersona(), licencia)) {
                    JOptionPane.showConfirmDialog(null, "Se edito correctamente");
                    limpiar();
                    cargarTablaPersona();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Inserte puntos");
            }
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

